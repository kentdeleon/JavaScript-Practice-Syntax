/* Comparison Operators 
== equal to
=== equal and same type to
!= not equal to
!== not equal and not same type 
> greater than
>= greater than or equal to
< less than
<= less than or equal to
*/

var x= 10;
var y =5;

if(x==y){
document.write("true x==y");
}
else if (x>=y){
document.write("true x>=y");
}
else{
document.write("true x!=y");
}