/*Arithmetic Operators 

+ addition
- subtraction
* multiplication
/ division
% modulus
++ increment
-- decrement

*/

var num1=10;
var num2=5;

document.write("10 + 5 = ");
document.write(num1+num2);
document.write("<br>");
document.write("10 - 5 = ");
document.write(num1-num2);
document.write("<br>");
document.write("10 * 5 = ");
document.write(num1*num2);
document.write("<br>");
document.write("10 / 5 = ");
document.write(num1/num2);
document.write("<br>");
document.write("10 % 5 = ");
document.write(num1%num2);
document.write("<br>");
document.write("++num1 - pre-increment num1 is now equal to ");
document.write(++num1);
document.write("<br>");
document.write("num1++ - post-increment num1 is still equal to ");
document.write(num1++);


/*Output

10 + 5 = 15
10 - 5 = 5
10 * 5 = 50
10 / 5 = 2
10 % 5 = 0
++num1 - pre-increment num1 is now equal to 11
num1++ - post-increment num1 is still equal to 11

*/