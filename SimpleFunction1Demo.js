/*Sample Function */

var num1= 5;
var num2= 2;


/*Create function ProdFunc that takes two arguments and return the product of two operands*/

function ProdFunc(num1,num2){
	return  num1 *num2;
}


var prod = ProdFunc(num1,num2); //call the ProdFunc function and store the result to prod variable

document.write(prod); //output the result of ProdFunc